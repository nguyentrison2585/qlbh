    public SanPham getSanPham() {
        String id, tenSP, loaiSP, ncc, donVi, donGia, soLuong, imagePath;
        int iDonGia, iSoLuong;
        if (cbbNhaCC_CTSP.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn nhà cung cấp!",
                    "Warning",JOptionPane.WARNING_MESSAGE);
            return null;
        }
        else {
            id = txtMaSP_CTSP.getText().trim().toUpperCase();
            tenSP = txtTenSP_CTSP.getText().trim();
            loaiSP = txtLoaiSP_CTSP.getText().trim();
            ncc = con.getMaNCC(cbbNhaCC_CTSP.getSelectedItem().toString());
            donVi = txtDonVi_CTSP.getText().trim();
            donGia = txtDonGia_CTSP.getText().trim();
            soLuong = txtSoLuong_CTSP.getText().trim();
            imagePath = txtImagePath.getText();
            if (id.equals("") || tenSP.equals("") || loaiSP.equals("") || ncc.equals("")
                    || donVi.equals("") || donGia.equals("") || soLuong.equals("")) {
                JOptionPane.showMessageDialog(null, "Bạn cần nhập đầy đủ thông tin!",
                        "Warning",JOptionPane.WARNING_MESSAGE);
                return null;
            }
            try {
                iDonGia = Integer.parseInt(donGia);
                iSoLuong = Integer.parseInt(soLuong);
                SanPham sanPham = new SanPham(id, tenSP, loaiSP, ncc, donVi, iDonGia, iSoLuong, imagePath);
                return sanPham;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Đơn giá và số lượng phải ở dạng số !",
                        "Warning",JOptionPane.WARNING_MESSAGE);
                return null;
            }
        }
    }
    
    public NhanVien getNhanVien() {
        if (cbbGioiTinh_TTNV.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn giới tính!",
                    "Warning",JOptionPane.WARNING_MESSAGE);
            return null;
        }
        else {
            String id, matKhau, hoTen, gioiTinh, ngaySinh, diaChi, soDT, tienLuong;
            int iTienLuong;
            id = txtMaNV_TTNV.getText().trim().toUpperCase();
            matKhau = txtMaKhau_TTNV.getText().trim();
            hoTen = txtHoTen_TTNV.getText().trim();
            gioiTinh = cbbGioiTinh_TTNV.getSelectedItem().toString();
            ngaySinh = txtNgaySinh_TTNV.getText().trim();
            diaChi = txtDiaChi_TTNV.getText().trim();
            soDT = txtSDT_TTNV.getText().trim();
            tienLuong = txtTienLuong_TTNV.getText().trim();
            if (id.equals("")||matKhau.equals("")||hoTen.equals("")||ngaySinh.equals("")
                    ||diaChi.equals("")||soDT.equals("")||tienLuong.equals("")) {
                JOptionPane.showMessageDialog(null, "Bạn cần nhập đầy đủ thông tin!",
                        "Warning",JOptionPane.WARNING_MESSAGE);
                return null;
            }
            else {
                try {
                    iTienLuong = Integer.parseInt(tienLuong);
                    NhanVien nhanVien = new NhanVien(id, matKhau, hoTen, gioiTinh, ngaySinh, diaChi, soDT, iTienLuong);
                    return nhanVien;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Tiền lương phải ở dạng số !",
                            "Warning",JOptionPane.WARNING_MESSAGE);
                    return null;
                }
            }
        }
    }
    
